public class AdditionOfTwoNumbers {
    public static void main(String[] args) {
        // Hard-coded numbers
        double num1 = 5.0;
        double num2 = 3.0;

        // Perform the addition
        double sum = num1 + num2;

        // Display the result
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}
